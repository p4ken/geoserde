pub struct GeometryDeserializer<R> {
    reader: R,
    coord_key: &'static str,
    coord_index: usize,
}

impl<R> GeometryDeserializer<R> {
    pub fn new(reader: R) -> Self {
        Self {
            reader,
            coord_key: "",
            coord_index: 0,
        }
    }
}

