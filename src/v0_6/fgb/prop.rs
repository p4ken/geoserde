use flatgeobuf::geozero::FeatureProperties;
use serde::Deserializer;

/// Adapter between geozero and serde
pub struct PropertiesAdapter<P> {
    geozero: P,
}
impl<'a, P: FeatureProperties> PropertiesAdapter<&'a P> {
    pub fn new(p: &'a P) -> Self {
        Self { geozero: p }
    }
}

