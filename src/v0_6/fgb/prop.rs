#[cfg(test)]
mod tests {
    use geozero::FeatureProperties;
    use serde::Deserialize;

    use super::PropertiesAdapter;

    #[derive(Deserialize)]
    struct MyObj {
        number: i32,
        text: String,
    }

    struct MyFmt;
    impl FeatureProperties for MyFmt {
        fn process_properties<P: geozero::PropertyProcessor>(
            &self,
            processor: &mut P,
        ) -> geozero::error::Result<bool> {
            if processor.property(0, "number", &geozero::ColumnValue::Int(9))? {
                return Ok(true);
            }
            if processor.property(0, "text", &geozero::ColumnValue::String("b"))? {
                return Ok(true);
            }
            Ok(false)
        }
    }

    #[test]
    fn geozero_deserializer_test() {
        let mut sut = PropertiesAdapter::new(&MyFmt);
        let obj = MyObj::deserialize(&mut sut).unwrap();
        assert_eq!(obj.number, 9);
        assert_eq!(obj.text, "b");
    }
}
